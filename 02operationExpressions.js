// Arithmetic operators
let x = 10;
let y = 5;
let addition = x + y;
let subtraction = x - y;
let multiplication = x * y;
let division = x / y;
let modulus = x % y;

console.log("Arithmetic Operators:");
console.log("Addition:", addition);
console.log("Subtraction:", subtraction);
console.log("Multiplication:", multiplication);
console.log("Division:", division);
console.log("Modulus:", modulus);

// Comparison operators
let a = 10;
let b = 5;

console.log("\nComparison Operators:");
console.log("a is equal to b:", a === b);
console.log("a is not equal to b:", a !== b);
console.log("a is greater than b:", a > b);
console.log("a is less than b:", a < b);
console.log("a is greater than or equal to b:", a >= b);
console.log("a is less than or equal to b:", a <= b);

// Logical operators
let isTrue = true;
let isFalse = false;

console.log("\nLogical Operators:");
console.log("AND Operator:", isTrue && isFalse);
console.log("OR Operator:", isTrue || isFalse);
console.log("NOT Operator for isTrue:", !isTrue);
console.log("NOT Operator for isFalse:", !isFalse);

// String concatenation
let firstName = "John";
let lastName = "Doe";
let fullName = firstName + " " + lastName;

console.log("\nString Concatenation:");
console.log("Full Name:", fullName);